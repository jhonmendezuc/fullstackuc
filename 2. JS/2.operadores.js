console.log(2 ** 2);
//operadores boleanos
console.log(2 > 2);
console.log(2 < 2);
console.log(2 == 2);
console.log(true);
console.log(false);
console.log(!false);
//operadores de relacion
console.log(2 >= 2); //mayor o igual
console.log(2 <= 2); //menor o igual
console.log(20 != 2); //diferente de
